# ============================================================
# 任务 5：描述性统计与分组统计
# 任务目标：对商品价格和一级品类形成一条可解释的结论。
# ============================================================
print('\n' + '=' * 60)
print('任务 5：描述性统计与分组统计')
print('=' * 60)

# 商品价格的描述性统计
print('\n--- 商品价格统计摘要 ---')
print(df['商品价格'].describe().round(2))

# 一级品类商品数
print('\n--- 各一级品类商品数 ---')
print(df['一级品类'].value_counts())

# 按一级品类分组，计算商品数、平均价格和中位价格
category_summary = (
    df.groupby('一级品类')
      .agg(商品数=('商品id', 'size'),
           平均价格=('商品价格', 'mean'),
           中位价格=('商品价格', 'median'))
      .sort_values('平均价格', ascending=False)
      .round(2)
)
print('\n--- 一级品类汇总（按平均价格降序）---')
print(category_summary)

# 规范结论：数据范围 - 字段/方法 - 结论 - 边界
print('\n--- 任务5规范结论 ---')
print('【结论1】')
print('数据范围：在本数据集的 25000 条淘宝商品记录中，')
print('字段与方法：使用"一级品类"和"商品价格"字段，'
      '按一级品类 groupby 聚合后计算平均价格。')
print('结论：数码家电的平均商品价格（约 3085.53 元）'
      '明显高于宠物用品（约 206.88 元），相差约 14.9 倍。')
print('边界：该结论基于商品标价，不代表实际成交金额或全网用户偏好。')


# ============================================================
# 五、挑战任务：做一张"省份—类别"小结表
# 在不修改原始 df 的前提下，选择广东和江苏两个省份，
# 比较它们的商品数量、平均价格与最常见一级品类。
# ============================================================
print('\n' + '=' * 60)
print('挑战任务：省份—类别小结表（广东 vs 江苏）')
print('=' * 60)

# 用 isin() 筛选两个省份
provinces = ['广东', '江苏']
subset = df[df['省份'].isin(provinces)]

# 按省份分组，统计商品数、平均价格和中位价格
province_summary = (
    subset.groupby('省份')
          .agg(商品数=('商品id', 'size'),
               平均价格=('商品价格', 'mean'),
               中位价格=('商品价格', 'median'))
          .round(2)
)
print('\n--- 省份汇总表 ---')
print(province_summary)

# 分别找出两个省份最常见的一级品类
for province in provinces:
    top_category = (
        subset.loc[subset['省份'] == province, '一级品类']
              .value_counts()
              .head(1)
    )
    print(f'\n{province} 最常见一级品类：')
    print(top_category)

# 两句带边界的结论
print('\n--- 挑战任务结论 ---')
print('【结论1 - 差异描述】')
print('数据范围：在本数据集 25000 条商品记录中，'
      '筛选广东和江苏两省共 4066 条记录。')
print('字段与方法：按"省份"分组，对"商品价格"求均值和中位数。')
print('结论：江苏省商品平均价格（936.48 元）略高于广东省（911.69 元），'
      '但广东的中位价格（608.62 元）略高于江苏（592.41 元），'
      '说明江苏价格分布可能更受高价商品拉动。')
print('边界：该结论仅基于商品标价，'
      '不等同于实际成交金额，也外推不到全国其他省份。')

print('\n【结论2 - 结论边界】')
print('数据范围：同上，广东和江苏两省的 4066 条商品记录。')
print('字段与方法：用 value_counts() 统计各一级品类商品数并取最高频。')
print('结论：广东最常见的一级品类为数码家电（168 件），'
      '江苏最常见的一级品类为图书音像（130 件），'
      '两省在主打商品类型上存在差异。')
print('边界：该结论反映的是采集样本中各品类的商品数量分布，'
      '不等于消费者真实购买量或搜索量，'
      '也不代表两省电商产业结构的全貌。')


